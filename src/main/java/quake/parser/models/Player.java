package quake.parser.models;

public class Player {

	private String name;
	private Integer kills;

	public String getName() {
		return name;
	}

	public void setName( String name ) {
		this.name = name;
	}

	public Integer getKills() {
		return kills;
	}

	public void setKills( Integer kills ) {
		this.kills = kills;
	}

}
